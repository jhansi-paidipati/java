class datatype{
    public static void main(String args[])
    {
        int num=1;
        float pencil=0.8f;
        double pen=8.0;
        boolean b=true;
        short c=4;
        long l=34567890890l;
        System.out.println(num);
        System.out.println(pencil);
        System.out.println(pen);
        System.out.println(c);
        System.out.println(l);
        System.out.print(b);
        
    }
}